/*
 * font_width() - Get font width
 *
 * INPUT
 *     - font: pointer to font structure
 * OUTPUT
 *     font width
 */
uint32_t font_width(const Font *font)
{
    /*Return worst case width using the | char as the reference.  */
    return  font_get_char(font, '|')->width;
}

/*
 * calc_str_width() - Get string width respect to font type and string size
 *
 * INPUT
 *     - font: pointer to font structure
 *     - str: pointer string
 * OUTPUT
 *     calculated string width
 */
uint32_t calc_str_width(const Font *font, const char *str)
{
    int width = 0;

    while(str[0] != '\0')
    {
        width += font_get_char(font, str[0])->width;
        str++;
    }

    return width;
}

/*
 * calc_str_line() - Calculates how many lines a string will occupy given a line width
 *
 * INPUT
 *     - font: pointer to font structure
 *     - str: pointer string
 *     - line_width: maximum line width before string is wrapped
 * OUTPUT
 *     line count
 */
uint32_t calc_str_line(const Font *font, const char *str, uint16_t line_width)
{
    uint8_t line_count = 1;
    uint16_t x_offset = 0;

    while(*str)
    {
        uint8_t character_width = font_get_char(font, str[0])->width;
        uint16_t word_width = character_width;
        char *next_character = (char *)str + 1;

        /* Allow line breaks */
        if(*str == '\n')
        {
            line_count++;
            x_offset = 0;
            str++;
            continue;
        }

        /* Calculate next work width */
        if(*str == ' ')
        {
            while(*next_character && *next_character != ' ' && *next_character != '\n')
            {
                word_width += font_get_char(font, *next_character)->width;
                next_character++;
            }
        }

        /* New line? */
        if(x_offset + word_width > line_width)
        {
            line_count++;
            x_offset = 0;
        }

        /* Remove leading spaces */
        if(x_offset == 0 && *str == ' ')
        {
            str++;
            continue;
        }

        x_offset += character_width;
        str++;
    }

    return line_count;
}
